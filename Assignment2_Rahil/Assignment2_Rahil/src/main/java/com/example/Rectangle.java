package com.example;

public class Rectangle {

  public String name;
  public String color;
  public int width;
  public int height;

  public void setName(String n) {
    this.name = n;
  }

  public void setColor(String c) {
    this.color = c;
  }

  public void setHeight(int h) {
    this.height = h;
  }

  public void setWidth(int w) {
    this.width = w;
  }

  public String getName() {
    return this.name;
  }

  public int getWidth() {
    return this.width;
  }

  public int getHeight() {
    return this.height;
  }

  public String getColor() {
    return this.color;
  }

}
